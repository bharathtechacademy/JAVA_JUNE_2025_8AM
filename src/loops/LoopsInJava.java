package loops;

public class LoopsInJava {
	
	//LOOPS IN JAVA ==> Repeatedly execute a block of code until a condition is met.
	
	// 1. For Loop ==> When we know the total number of iterations to be executed before itself.(basic for loop, for-each loop)
	// 2. While Loop ==> When we do not know the total number of iterations to be executed before itself. (basic while loop, do-while loop)
	
	public static void main(String[] args) {
		
		String name = "Bharath";
		
		System.out.println(name);
		System.out.println(name);
		System.out.println(name);
		System.out.println(name);
		System.out.println(name);
		
		System.out.println(name);
		System.out.println(name);
		System.out.println(name);
		System.out.println(name);
		System.out.println(name);
		
	}

}
